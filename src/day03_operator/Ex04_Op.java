package day03_operator;
//관계연신자
public class Ex04_Op {
	public static void main(String[] args) {
		/*
		 true(참), false(거짓)
		 a = 10, b = 20;
		 a > b : false
		 a <= b : true
		 a == b : false
		 a != b : true
		 */
		
		double do1 = 3.0, do2 = 3.1;
		System.out.println(do1 > do2);
		System.out.println(do1 <= do2);
		System.out.println(do1 == do2);
		System.out.println(do1 != do2);
		
	}

}
