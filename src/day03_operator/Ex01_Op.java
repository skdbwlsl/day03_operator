//연산자 
public class Ex01_Op {
	public static void main(String[] args) {
		/*
		   / (나누기) : 정수 /정수 = 정수(몫)
		   	- 실수 / 정수 = 실수
		   %(mod) : 정수 % 정수 = 나머지
		   예) 
		   - 10 /3 = 3(몫)
		   - 10/ 3.0 = 3.3333...
		   - 10 % 3= 1
		   %연산자를 이용하여 어떤 수의 배수 또는 짝, 홀을 구분
		   10(N) % 2 = 0 : 짝수 
		   10(N) %2 = 1 : 홀수
		   
		   12(N) % 2 = 0 : 짝수이면서 2의 배수
		   12(N) % 3 = 0 : 3의 배수
		   
		   
		 */
	}

}
